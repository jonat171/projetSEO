                                        <h2 class="post-headline">Mozzarella au lait de bufflone</h2>
                                    </a>
                                    <p>La mozzarella est un fromage à pâte filée d'origine italienne à base de lait de vache ou de bufflonne. La mozzarella di Bufala Campana, produite en Campanie avec du lait de bufflonne, est l'objet d'une appellation d'origine protégée (AOP) depuis 1996. En Italie, la mozzarella de lait de vache est fabriquée et commercialisée sous le nom de « mozzarella », ou « mozzarella di latte vaccino », ou « fior di latte ». Ailleurs dans le monde, la mozzarella est un fromage industriel qui fait partie des fromages de lait de vache les plus produits et consommés. Ce fromage industriel, commercialisé sous le nom de mozzarella, crée une confusion avec la mozzarella traditionnelle au lait de bufflonne.</p>

                                    <blockquote class="yummy-blockquote mt-30 mb-30">
                                        <h5 class="mb-30">“La mozzarella, c'est le fromage des enfants, c'est comme lorsqu'on mord dans le sein maternel, il y a des gouttes de lait qui en coulent ; c'est lié à se souvenir d'enfance. J'aime ce fromage que les grands amateurs trouvent désuet et sans goût particulier, je l'aime parce qu'il me ramène au sein de ma mère.”</h5>
                                        <h6 class="text-muted">Tahar Ben Jelloun</h6>
                                    </blockquote>

                                    <h4>Mozzarella au lait de bufflone</h4>
                                    <p>La mozzarella tire son nom d'une étape particulière de son cycle de production. Après l'étape du filage, la pâte est « mozzata », c'est-à-dire « coupée » en morceaux commercialisables.
                                    <br>
									Le mot « mozzarella », féminin en italien comme en français, est parfois francisé en mozzarelle mais l'appellation exacte est mozzarella di Bufala Campana protégée par le label de qualité STG (Specialità tradizionale garantita) depuis 1998. De nos jours, la mozzarella est majoritairement fabriquée hors d'Italie avec du lait de vache et non de bufflonne.
									<br>
									Le nom mozzarella n'a pourtant jamais été déposé.</p>

                                    <img class="br-30 mb-30" src="img/blog-img/11.jpg" alt="mozzarella">
                                    <p>C'est un fromage frais à pâte filée de saveur douce et acidulée produit traditionnellement en Italie dans les régions de Campanie, Pouilles, Basilicate, Calabre, Abruzzes, Molise, Marches et dans le sud du Latium.
                                    <br>
									C'est un fromage à base de lait de bufflonne ou de lait de vache, à pâte filée, d'un poids moyen de 400 à 800 g. Il est fabriqué à base de lait de bufflonne principalement dans la région de Campanie et dans les environs. Il faut environ dix litres de lait pour produire un kilogramme de fromage.
									<br>
									Seule la mozzarella de bufflonne dispose de l'appellation d'origine protégée DOP (Denominazione di Origine Protetta). Elle porte le nom de Mozzarella di Bufala Campana.
									<br>
									La particularité de la mozzarella est d'être un fromage à pâte filée. Le lait est d'abord emprésuré. Le caillé obtenu est découpé puis trempé dans de l'eau très chaude (80-90°C). Il est alors filé : le filage consiste à soulever et à tirer la pâte à plusieurs reprises, en s'aidant d'une écuelle et d'un bâton, jusqu'à ce que l'on obtienne un mélange filant et homogène. Après avoir été soumise au filage, elle est « mozzata », c'est-à-dire « coupée » pour lui donner la bonne dimension. Lorsqu'elle est coupée à la main cela laisse une empreinte caractéristique.
									<br>
									Depuis des siècles, les élevages de bufflonnes se trouvent en Campanie, importés par les Lombards, pour la plupart dans la province de Caserta (par exemple la mozzarella d'Aversa est réputée), mais aussi vers Paestum et Battipaglia.</p>

                                    <img class="br-30 mb-30" src="img/blog-img/12.jpg" alt="mozzarella">
                                    <p>Elle peut se consommer nature, crue ou cuite, et entre dans la composition de nombreuses recettes de salades, pâtes, pizzas, etc.
                                    <br>
									Il existe un très grand écart de qualité et de saveur entre la production fermière et les fabrications artisanales et industrielles. La mozzarella traditionnelle n'est faite qu'avec du lait de bufflonne, tandis que les fromageries industrielles utilisent aussi bien du lait de bufflonne que du lait de vache.
									<br>
									Le goût de la mozzarella varie en fonction de sa qualité, allant d'une saveur relativement neutre jusqu'au goût prononcé de lait en passant par de subtils arômes de noisette.
									<br>
									Les mozzarellas fermière et artisanale doivent être consommées très rapidement après leur fabrication, idéalement le jour même.
									<br>
									La mozzarella est le fromage le plus consommé en Italie, présent sur les tables de six Italiens sur dix.</p>

